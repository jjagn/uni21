function [K] = local_frame(E, I, A, L)
% outputs stiffness matrix K for a given frame given input variables E, I,
% A and L
B = (A*L^2)/I;
K = (E*I)/(L^3) * [B 0 0 -B 0 0;...
             0 12 6*L 0 -12 6*L;...
             0 6*L 4*L^2 0 -6*L 2*L^2;...
             -B 0 0 B 0 0;...
             0 -12 -6*L 0 12 -6*L;...
             0 6*L 2*L^2 0 -6*L 4*L^2];
end

